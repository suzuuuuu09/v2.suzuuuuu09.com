export const getFileTreeIcon = (
	fileName: string,
	isOpenDirectory = false,
): FileTreeIcon => {
	if (fileName.endsWith("/")) {
		return isOpenDirectory
			? FILE_TREE_DEFAULT_ICONS.folderOpen
			: FILE_TREE_DEFAULT_ICONS.folder;
	}

	const normalizedName = fileName.trim().toLowerCase();

	if (FILE_TREE_ALL_ICONS[normalizedName]) {
		return FILE_TREE_ALL_ICONS[normalizedName];
	}

	const extension = normalizedName.split(".").pop() ?? "";
	if (FILE_TREE_ALL_ICONS[extension]) {
		return FILE_TREE_ALL_ICONS[extension];
	}

	return FILE_TREE_DEFAULT_ICONS.file;
};
